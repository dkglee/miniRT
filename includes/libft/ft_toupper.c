#include "libft.h"

int		is_lowcase(int c)
{
	return ((97 <= c) && (122 >= c));
}

int		ft_toupper(int c)
{
	if (is_lowcase(c))
	{
		c -= 32;
		return (c);
	}
	return (c);
}
