#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	len;
	size_t	i;

	if (s == 0 || f == 0)
		return (0);
	len = ft_strlen(s);
	i = 0;
	if (0 == (str = (char *)malloc(sizeof(char) * len + 1)))
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
